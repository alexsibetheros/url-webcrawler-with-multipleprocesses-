void print_list(l_node_point start)
{
	printf("\n\n----------------------------->PrintList<-------------------------------\n");
	while(start!=NULL)
	{
		print_info(start->url);
		start=start->next;
	}
	printf("----------------------------->END PRINT LIST<--------------------------------\n");

}
