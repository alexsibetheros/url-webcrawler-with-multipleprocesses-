l_node_point create_list(char* get_url)
{
	get_url[strlen(get_url)]='\0';
	l_node_point temp= (l_node_point)malloc(sizeof(l_node));
	temp->next=NULL; 
	int size=strlen(get_url);
	temp->url=(char*)malloc((size+1)*sizeof(char));
	
	strcpy(temp->url,get_url);
	return temp;
}
