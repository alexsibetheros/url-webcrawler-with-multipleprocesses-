void destroy_node( l_node_point node)
{	
	free((node)->url);
	free(node);
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
void delete_list(l_node_point pointer)
{	
	l_node_point run=pointer;
	while(run!=NULL)
	{
		pointer=run->next;
		destroy_node(run);
		run=pointer;
	}
	
}
